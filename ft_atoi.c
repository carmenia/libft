#include "libft.h"

int	ft_atoi(const char *str)
{
	int	n;
	int	i;

	n = 0;
	i = 0;
	while (str[i] <= 32)
		i++;
	while (str[i] > 47 && str[i] < 58)
	{
		n = n + (str[i] - '0');
		n = n * 10;
		i++;
	}
	n = n / 10;
	return (n);
}
