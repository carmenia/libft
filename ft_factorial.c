#include "libft.h"

size_t	ft_factorial(size_t n)
{
	int	i;

	i = 1;
	if (n >= 13)
		return (0);
	if (n == 0 || n == 1)
		return (1);
	while (n > 1)
	{
		i = n * i;
		n--;
	}
	return (i);
}
